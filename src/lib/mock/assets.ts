/**
 * Get asset by ID
 */
export function getAssetById(id: string): Asset | undefined {
  return MOCK_ASSETS.find((asset) => asset.id === id)
}

/**
 * Get asset by slug
 */
export function getAssetBySlug(slug: string): Asset | undefined {
  return MOCK_ASSETS.find((asset) => asset.slug === slug)
}

/**
 * Get assets by contributor ID
 */
export function getAssetsByContributor(contributorId: string): Asset[] {
  return MOCK_ASSETS.filter((asset) => asset.contributorId === contributorId)
}

/**
 * Get assets by category
 */
export function getAssetsByCategory(category: string): Asset[] {
  return MOCK_ASSETS.filter((asset) => asset.category.toLowerCase() === category.toLowerCase())
}

/**
 * Get free assets
 */
export function getFreeAssets(): Asset[] {
  return MOCK_ASSETS.filter((asset) => asset.isFree)
}

/**
 * Search assets by query
 */
export function searchAssets(query: string): Asset[] {
  const lowerQuery = query.toLowerCase()
  return MOCK_ASSETS.filter(
    (asset) =>
      asset.title.toLowerCase().includes(lowerQuery) ||
      asset.description?.toLowerCase().includes(lowerQuery) ||
      asset.tags.some((tag) => tag.toLowerCase().includes(lowerQuery)) ||
      asset.contributor.toLowerCase().includes(lowerQuery)
  )
}

